#include <iostream>

int main() {
    int x = 5;
    float y = 10.5;
    double z = 15.32423;
    bool condition = true;
    char v = 'c';
    std::cout << "The value of int is: " << x << std::endl;
    std::cout << "The value of float is: " << y << std::endl;
    std::cout << "The value of double is: " << z << std::endl;
    std::cout << "The boolean value is: " << condition << std::endl;
    std::cout << "The value of char is: " << v << std::endl;
}